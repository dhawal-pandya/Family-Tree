class Person:
    def __init__(self, first_name, last_name, yob, yod, gender, parents, spouses, children, uid):
        self.first_name = first_name
        self.last_name = last_name
        self.yob = yob
        self.yod = yod
        self.gender = gender
        self.parents = parents
        self.spouses = spouses
        self.children = children
        self.uid = uid